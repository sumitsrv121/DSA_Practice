    private static String removeInfinity(int[] arr, int key) {
        int start = 0;
        int end = 1;
        while (end < arr.length && key > arr[end]) {
            start = end;
            end *= 2;
        }
        return start + "-" + end;
    }

    private static int binarySearch(int[] arr, int key, int start, int end) {
        while (start <= end) {
            int mid = start + (end - start) / 2;
            if (mid >= arr.length) {
                end = mid - 1;
            } else if (arr[mid] == key) {
                return mid;
            } else if (arr[mid] > key) {
                end = mid - 1;
            } else {
                start = mid + 1;
            }
        }
        return -1;
    }

